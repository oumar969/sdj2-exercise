public class Person {
    private String name;
    private int alder;


    public Person(String name, int alder) {
        this.name = name;
        this.alder = alder;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAlder() {
        return alder;
    }

    public void setAlder(int alder) {
        this.alder = alder;
    }
}
